package clienteempregado;

public class Persoa {

	private String nome; 
	private String dni;
	private String localidade;
	private int telefono;
	
	public Persoa(String nome, String dni, String localidade, int telefono) {
		super();
		this.nome = nome;
		this.dni = dni;
		this.localidade = localidade;
		this.telefono = telefono;
	}
	
}
